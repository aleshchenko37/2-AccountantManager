public class YearReportSaved {

    public String month;
    public int amount;
    public boolean isExpense;

    public String year;



    YearReportSaved(String month, int amount, boolean isExpense, String year) {
        this.month = month;
        this.amount = amount;
        this.isExpense = isExpense;
        this.year = year;
    }
}
